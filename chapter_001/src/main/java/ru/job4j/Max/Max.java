public class Max {
private int value1;
private int value2;
private int value3;


public int Compare(int value1, int value2) {
int result;
result=value1 > value2 ? value1:value2;
return result;
}

public int max(int value1, int value2, int value3){
int result1;
int result2;
result1=Compare(value1,value2);
result2=Compare(result1,value3);
return result2;
}
}