package ru.track;

public class Profession {
    public String name;
    public String profession;

    public Profession( String name, String profession) {
        this.name = name;
        this.profession = profession;
    }

    public Profession() {
    }

    public String getname() {
        return this.name;
    }
    public String getpro() {
        return this.profession;
    }
}


